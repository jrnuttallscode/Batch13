//Abstraction is a process of hiding the implementation of the methods
//by having a keyword abstract in the method and removing the body or implementation of method
//and ending it with a semicolon 
//you cannot instantiate the object of this class
//When a class is abstract you may or many not have methods as abstract
//But if you have 1 method as abstract it forces you to have the class as abstract


